        <h2>ABOUT IQAC:</h2>
        <p>Kongu Engineering College have
            established Internal Quality Assurance Cell (IQAC) from 2015 for performance
            evaluation, assessment & accreditation and quality up-gradation. Since quality
            enhancement is a continuous process, the IQAC becomes a part of the institutionâ€™s
            system & work towards realisation of the goals of quality enhancement & sustenance.
        </p>
        </div>

        <hr>
        <BR><BR>
        <h2>OBJECTIVES:</h2>
        <ul>
            <li>To enhance the quality of teaching, learning, evaluation and research activities
                across the campus.</li>
            <li>To establish a system for deliberate, consistent and transformative enhancement
                of the institution's overall performance</li>
            <li> To promote the integration of a quality-focused culture and the adoption of
                best practices within the institution.</li>
        </ul>

</body>
<hr>
<BR><BR>
<h2>STRATEGIES:</h2>
<ul>
    <li>Ensuring timely, efficient, and progressive performance of academic,
        administrative,
        and
        financial tasks.</li>
    <li>The relevance and quality of academic and research programmes.</li>
    <li>Optimization and integration of modern methods of teaching and learning.</li>
    <li>Ensuring the credibility of evaluation procedures.</li>
    <li>Ensuring the adequacy, maintenance, and functioning of the support structure and
        services.
    </li>
    <li>Research sharing and networking with other institutions in India and abroad.
    </li>
</ul>
<hr>
<BR><BR>
<h2>FUNCTIONS:</h2>
<ul>
    <li>Development and application of quality benchmarks/parameters for various
        academic
        and
        administrative activities of the institution.</li>
    <li>Facilitating the creation of a learner-centric environment conducive to quality
        education
        and faculty maturation to adopt the required knowledge and technology for
        participatory
        teaching and learning process.</li>
    <li>Arrangement for feedback response from students, parents, and other stakeholders
        on
        quality-related institutional processes.</li>
    <li>Dissemination of information on various quality parameters of higher education.
    </li>
    <li>Organization of inter and intra-institutional workshops, seminars on
        quality-related
        themes.
    </li>
    <li>Documentation of the various programmes/activities leading to quality
        improvement.
    </li>
    <li>Acting as a nodal agency of the Institution for coordinating quality-related
        activities,
        including adoption and dissemination of best practices.</li>
    <li>Development and maintenance of institutional database through Management
        Information
        System
        (MIS) for the purpose of maintaining/enhancing the institutional quality.</li>
    <li>Development of Quality Culture in the institution.</li>
    <li>Preparation of the Annual Quality Assurance Report (AQAR) as per guidelines and
        parameters
        of NAAC, to be submitted to NAAC.</li>
</ul>
<hr>
<BR></BR>
<h2>BENEFITS:</h2>
<ul>
    <li>Ensure heightened level of clarity and focus in institutional functioning
        towards
        quality enhancement.</li>
    <li> Ensure internalization of the quality culture.</li>
    <li>Ensure enhancement and coordination among various activities of the institution
        and
        institutionalize all good practices.</li>
    <li>Provide a sound basis for decision-making to improve institutional functioning.
    </li>
    <li>Act as a dynamic system for quality changes in Institution.
    </li>
    <li> Build an organised methodology of documentation and internal communication.
    </li>
</ul>

